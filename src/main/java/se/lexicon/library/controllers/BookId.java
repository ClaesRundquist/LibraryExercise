package se.lexicon.library.controllers;
/* Wrapper for Integer book id */
public class BookId {
	private Integer value;

	public Integer getValue() {
		return value;
	}

	public void setValue(Integer value) {
		this.value = value;
	}

	
	public BookId() {
		super();
	}

	public BookId(Integer value) {
		super();
		this.value = value;
	}
	
	

}
